-- 5. Jobs (Careers) Table
-- ==========================================
CREATE TABLE IF NOT EXISTS jobs (
    id UUID DEFAULT uuid_generate_v4() PRIMARY KEY,
    title TEXT NOT NULL,
    department TEXT NOT NULL,
    location TEXT NOT NULL,
    type TEXT NOT NULL,
    description TEXT NOT NULL,
    requirements TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Enable RLS for jobs
ALTER TABLE jobs ENABLE ROW LEVEL SECURITY;

-- Grant permissions to fix "permission denied"
GRANT ALL ON TABLE jobs TO anon, authenticated, service_role;

-- Drop existing policies first
DROP POLICY IF EXISTS "Allow public read access to jobs" ON jobs;
DROP POLICY IF EXISTS "Allow public insert to jobs" ON jobs;
DROP POLICY IF EXISTS "Allow public update to jobs" ON jobs;
DROP POLICY IF EXISTS "Allow public delete to jobs" ON jobs;

-- Allow public read access to jobs
CREATE POLICY "Allow public read access to jobs" ON jobs FOR SELECT USING (true);

-- Allow public insert, update, delete for demo/development purposes
CREATE POLICY "Allow public insert to jobs" ON jobs FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update to jobs" ON jobs FOR UPDATE USING (true);
CREATE POLICY "Allow public delete to jobs" ON jobs FOR DELETE USING (true);

