-- ReCognate Web Application - Supabase Schema & RLS Policies

-- 1. Create Tables
CREATE TABLE IF NOT EXISTS team_members (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  role TEXT NOT NULL,
  expertise TEXT NOT NULL,
  image_url TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS projects (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title TEXT NOT NULL,
  category TEXT NOT NULL,
  description TEXT NOT NULL,
  technologies TEXT[] DEFAULT '{}',
  status TEXT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS products (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  subtitle TEXT NOT NULL,
  description TEXT NOT NULL,
  status TEXT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS rd_prototypes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  title TEXT NOT NULL,
  description TEXT NOT NULL,
  status TEXT NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. Enable Row Level Security (RLS)
ALTER TABLE team_members ENABLE ROW LEVEL SECURITY;
ALTER TABLE projects ENABLE ROW LEVEL SECURITY;
ALTER TABLE products ENABLE ROW LEVEL SECURITY;
ALTER TABLE rd_prototypes ENABLE ROW LEVEL SECURITY;

-- 3. Grant Table Permissions
-- This is necessary to fix "permission denied for table" errors
GRANT ALL ON TABLE team_members TO anon, authenticated, service_role;
GRANT ALL ON TABLE projects TO anon, authenticated, service_role;
GRANT ALL ON TABLE products TO anon, authenticated, service_role;
GRANT ALL ON TABLE rd_prototypes TO anon, authenticated, service_role;


-- 4. Create RLS Policies

-- Drop existing policies first to allow re-running the script
DROP POLICY IF EXISTS "Allow public read access to team_members" ON team_members;
DROP POLICY IF EXISTS "Allow public read access to projects" ON projects;
DROP POLICY IF EXISTS "Allow public read access to products" ON products;
DROP POLICY IF EXISTS "Allow public read access to rd_prototypes" ON rd_prototypes;

DROP POLICY IF EXISTS "Allow public insert to team_members" ON team_members;
DROP POLICY IF EXISTS "Allow public update to team_members" ON team_members;
DROP POLICY IF EXISTS "Allow public delete to team_members" ON team_members;

DROP POLICY IF EXISTS "Allow public insert to projects" ON projects;
DROP POLICY IF EXISTS "Allow public update to projects" ON projects;
DROP POLICY IF EXISTS "Allow public delete to projects" ON projects;

DROP POLICY IF EXISTS "Allow public insert to products" ON products;
DROP POLICY IF EXISTS "Allow public update to products" ON products;
DROP POLICY IF EXISTS "Allow public delete to products" ON products;

DROP POLICY IF EXISTS "Allow public insert to rd_prototypes" ON rd_prototypes;
DROP POLICY IF EXISTS "Allow public update to rd_prototypes" ON rd_prototypes;
DROP POLICY IF EXISTS "Allow public delete to rd_prototypes" ON rd_prototypes;

-- Public Read Access: Allow anyone (anon) to read data
CREATE POLICY "Allow public read access to team_members" ON team_members FOR SELECT USING (true);
CREATE POLICY "Allow public read access to projects" ON projects FOR SELECT USING (true);
CREATE POLICY "Allow public read access to products" ON products FOR SELECT USING (true);
CREATE POLICY "Allow public read access to rd_prototypes" ON rd_prototypes FOR SELECT USING (true);

-- Admin Write Access: Allow anyone (for demo/development purposes) to insert, update, and delete
-- WARNING: In a real production app, replace 'true' with 'auth.role() = ''authenticated''' to restrict to logged-in users.
CREATE POLICY "Allow public insert to team_members" ON team_members FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update to team_members" ON team_members FOR UPDATE USING (true);
CREATE POLICY "Allow public delete to team_members" ON team_members FOR DELETE USING (true);

CREATE POLICY "Allow public insert to projects" ON projects FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update to projects" ON projects FOR UPDATE USING (true);
CREATE POLICY "Allow public delete to projects" ON projects FOR DELETE USING (true);

CREATE POLICY "Allow public insert to products" ON products FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update to products" ON products FOR UPDATE USING (true);
CREATE POLICY "Allow public delete to products" ON products FOR DELETE USING (true);

CREATE POLICY "Allow public insert to rd_prototypes" ON rd_prototypes FOR INSERT WITH CHECK (true);
CREATE POLICY "Allow public update to rd_prototypes" ON rd_prototypes FOR UPDATE USING (true);
CREATE POLICY "Allow public delete to rd_prototypes" ON rd_prototypes FOR DELETE USING (true);

-- You can now run `node migrate-data.mjs` to seed the database safely.

